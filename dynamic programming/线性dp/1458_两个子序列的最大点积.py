class Solution:
    def maxDotProduct(self, nums1: List[int], nums2: List[int]) -> int:
        # dp[i][j] è¡¨ç¤ºè€ƒè™‘ nums1 å‰ i ä¸ªå…ƒç´ å’Œ nums2 å‰ j ä¸ªå…ƒç´ æ—¶ï¼Œèƒ½å¤Ÿå¾—åˆ°çš„æœ€å¤§ç‚¹ç§¯ã€‚
        # é€‰æ‹©å½“å‰å…ƒç´ , åˆ™æœ€å¤§ç‚¹ç§¯å¯èƒ½ä¸º max(dp[i-1][j-1] + nums1[i] * nums2[j], nums1[i] * nums2[j])
        # ä¸é€‰ nums[1], åˆ™æœ€å¤§ç‚¹ç§¯ä¸º dp[i-1][j]
        # ä¸é€‰ nums2[1], åˆ™æœ€å¤§ç‚¹ç§¯ä¸º dp[i][j-1]
        m, n = len(nums1), len(nums2)
        dp = [[float('-inf') for _ in range(n + 1)] for _ in range(m + 1)]
        for i in range(1, m + 1):
            for j in range(1, n + 1):
                cur = nums1[i-1] * nums2[j-1]
                dp[i][j] = max(cur, dp[i - 1][j - 1] + cur, dp[i-1][j], dp[i][j-1])
        return dp[-1][-1]


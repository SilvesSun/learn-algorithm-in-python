from typing import List


class Solution:
    def maxMoves(self, grid: List[List[int]]) -> int:
        # å½“å‰å•å…ƒæ ¼å¯ä»¥ç§»åŠ¨åˆ°å…¶å³ä¸Šæ–¹ã€æ­£å³æ–¹ä»¥åŠå³ä¸‹æ–¹ä¸‰ä¸ªä½ç½®ä¸­ä¸¥æ ¼å¤§äºå…¶çš„å•å…ƒæ ¼ã€‚é‚£ä¹ˆæ¢ä¸ªè§’åº¦æƒ³ï¼Œä¸€ä¸ªå•å…ƒæ ¼å¯ä»¥ä»å…¶å·¦ä¸Šæ–¹ã€æ­£å·¦æ–¹ä»¥åŠå·¦ä¸‹æ–¹çš„å•å…ƒæ ¼è½¬ç§»è¿‡æ¥ã€‚
        # ä¸€æ—¦å•å…ƒæ ¼æ»¡è¶³äº†å¤§äºå…¶å·¦ä¾§ä¸‰ä¸ªå•å…ƒæ ¼ä½ç½®çš„ï¼Œé‚£ä¹ˆèƒ½å¤Ÿåˆ°è¾¾å½“å‰å•å…ƒæ ¼å°±å–å†³äºèƒ½å¦åˆ°è¾¾å…¶ä¹‹å‰ä½ç½®çš„å•å…ƒæ ¼æ˜¯å¦èƒ½å¤Ÿåˆ°è¾¾ã€‚è¿™å°±å˜æˆæ‹†åˆ†å­é—®é¢˜äº†ï¼Œ
        # è®° canMove[i][j] è¡¨ç¤ºèƒ½å¤Ÿåˆ°è¾¾å•å…ƒæ ¼ (i, j)ï¼Œé‚£ä¹ˆçŠ¶æ€è½¬ç§»æ–¹ç¨‹å³ä¸ºï¼šcanMove[i][j] |= canMove[i-p][j - 1] && (grid[i][j] > grid[i-p][j-1])ï¼Œp=-1,0,1ã€‚
        # å¹¶ä¸”åªè¦èƒ½åˆ°è¾¾å•å…ƒæ ¼ (i, j) ï¼Œå°±è¯´æ˜èƒ½åˆ°è¾¾ç´¢å¼• j åˆ—ï¼Œæœ€å¤§ç§»åŠ¨æ¬¡æ•°è‡³å°‘ä¸º jã€‚
        # åˆå§‹æ¡ä»¶é¦–åˆ—ä¸€å®šæ˜¯å¯è¾¾çš„ï¼Œ canMove[i][0] = true
        m = len(grid)
        n = len(grid[0])
        can_move = [[0 for _ in range(n)] for _ in range(m)]
        ret = 0
        for j in range(1, n):
            for i in range(n):
                for p in [-1, 0, 1]:
                    if i + p < 0 or i + p >= m:
                        continue
                    if grid[i][j] > grid[i + p][j - 1] and (j == 1 or can_move[i + p][j - 1]):
                        can_move[i][j] = 1
                        ret = j
                        break
        return ret
